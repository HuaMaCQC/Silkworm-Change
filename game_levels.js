    init() {
        //遮罩
        var myMask = document.getElementById("myMask");
        var Mask = myMask.getContext("2d");

        Mask.clearRect(0, 0, myMask.width, myMask.height)
        Mask.fillStyle = "#5b5c60";
        Mask.rect(0, 0, myMask.width, myMask.height);
        Mask.fill();

        var r_lang = this.range.length;
        var path = new Path2D();
        path.moveTo(this.range[r_lang - 1][0], this.range[r_lang - 1][1])
        for (var i = 0; i < r_lang; i++) {
            path.lineTo(this.range[i][0], this.range[i][1]);
        }
        Mask.fillStyle = '#d9d9d9';
        Mask.fill(path);

        this.winPos = [];
        for (var i = 0; i < 1201; i = i + 25) {
            for (var a = 0; a < 721; a = a + 25) {
                if (Mask.isPointInPath(path, i, a)) {
                    this.winPos.push([i, a]);
                }
            }
        }
        Mask.beginPath();
        Mask.moveTo(0, 0);
        Mask.lineTo(0, 700);
        Mask.lineTo(1200, 700);
        Mask.lineTo(1200, 0);
        Mask.lineTo(0, 0);
        Mask.strokeStyle = "#ff0000";
        Mask.stroke();
        
    }
